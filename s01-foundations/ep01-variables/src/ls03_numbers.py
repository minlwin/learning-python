def plus(a:int, b:int) -> int:
    return a + b

def minus(a:int, b:int) -> int:
    return a - b

def multiply(a:int, b:int) -> int:
    return a * b

def exponent(a:int, b:int) -> int:
    return a ** b

def divide(a:int, b:int) -> float:
    return a / b

def divide_as_int(a:int, b:int) -> int:
    return a // b